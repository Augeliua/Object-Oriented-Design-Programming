package sc2002.bto.enums;
/**
 * Represents the possible statuses of an enquiry in the BTO system.
 * 
 */
public enum EnquiryStatus {
    /**
     * Enquiry has been submitted but not yet answered
     */
    PENDING,
    /**
     * Enquiry has been answered by an HDB Officer or Manager
     */
    REPLIED
}
